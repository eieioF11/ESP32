void drawcomp(TFT_eSPI *tft,int fx,int fy,int color,float c)
{
  float sc=sinf(c*DEG_TO_RAD);
  float cc=cosf(c*DEG_TO_RAD);
  for(int y=0;y<50;y++)
  {
    for(int x=0;x<50;x++)
    {
      if(compass[y][x])
      {
        int X=x-25;
        int Y=y-25;
        int x_=X*cc-Y*sc;
        int y_=X*sc+Y*cc;
        tft->drawPixel(fx+x_,fy+y_,color);
      }
    }
  }
}

