bool drawCLOCK(TFT_eSPI *tft,int fx,int fy,int hcolor,int mcolor,int scolor,int fcolor,uint8_t hh,uint8_t mm,uint8_t ss,int backcolor=TFT_BLACK)
{
  bool secupdate=false;
  static uint8_t sold=0; 
  if(sold!=ss)
  {
    secupdate=true;
    sold=ss;

    if(hh>12)
      hh-=12;
    float shc=(float)hh*(360.f/12.f);
    float lc=(float)mm*(360.f/60.f);
    float sec=(float)ss*(360.f/60.f);
    float lsc=sinf(lc*DEG_TO_RAD);
    float lcc=cosf(lc*DEG_TO_RAD);
    float shsc=sinf(shc*DEG_TO_RAD);
    float shcc=cosf(shc*DEG_TO_RAD);
    float sesc=sinf(sec*DEG_TO_RAD);
    float secc=cosf(sec*DEG_TO_RAD);
    
    //clear
    for(int y=0;y<50;y++)
    {
      for(int x=0;x<50;x++)
      {
        if(flame[y][x])
          tft->drawPixel(fx+x-25,fy+y-25,fcolor);
        else 
          tft->drawPixel(fx+x-25,fy+y-25,backcolor);
      }
    }
    //draw
    for(int y=0;y<50;y++)
    {
      for(int x=0;x<50;x++)
      {
        if(longhand[y][x])
        {
          int X=x-25;
          int Y=y-25;
          int x_=X*lcc-Y*lsc;
          int y_=X*lsc+Y*lcc;
          tft->drawPixel(fx+x_,fy+y_,mcolor);
        }
        if(shorthand[y][x])
        {
          int X=x-25;
          int Y=y-25;
          int x_=X*shcc-Y*shsc;
          int y_=X*shsc+Y*shcc;
          tft->drawPixel(fx+x_,fy+y_,hcolor);
        }
        if(secondhand[y][x])
        {
          int X=x-25;
          int Y=y-25;
          int x_=X*secc-Y*sesc;
          int y_=X*sesc+Y*secc;
          tft->drawPixel(fx+x_,fy+y_,scolor);
        }
        //if(flame[y][x]==1)
          //tft->drawPixel(fx+x-25,fy+y-25,fcolor);
      }
    }
  }
  return secupdate;
}

